package com.peoplepowerco.virtuoso.models;


public class PPWifiParamModel {

    private String wifiSsidValues;
    private String wifiQuality;
    private String wifiSecurity;
    private String wifiSecurityValues;
    private String wifiChannel;
    private String wifiSignal;
    private String wifiSignalValues;
    private boolean wifiOther;


    public String getWifiSsidValues() {
        return wifiSsidValues;
    }

    public void setWifiSsidValues(String wifiSsidValues) {
        this.wifiSsidValues = wifiSsidValues;
    }

    public String getWifiQuality() {
        return wifiQuality;
    }

    public void setWifiQuality(String wifiQuality) {
        this.wifiQuality = wifiQuality;
    }

    public String getWifiSecurity() {
        return wifiSecurity;
    }

    public void setWifiSecurity(String wifiSecurity) {
        this.wifiSecurity = wifiSecurity;
    }

    public String getWifiSecurityValues() {
        return wifiSecurityValues;
    }

    public void setWifiSecurityValues(String wifiSecurityValues) {
        this.wifiSecurityValues = wifiSecurityValues;
    }

    public String getWifiChannel() {
        return wifiChannel;
    }

    public void setWifiChannel(String wifiChannel) {
        this.wifiChannel = wifiChannel;
    }

    public String getWifiSignal() {
        return wifiSignal;
    }

    public void setWifiSignal(String wifiSignal) {
        this.wifiSignal = wifiSignal;
    }

    public String getWifiSignalValues() {
        return wifiSignalValues;
    }

    public void setWifiSignalValues(String wifiSignalValues) {
        this.wifiSignalValues = wifiSignalValues;
    }

    public boolean isWifiOther() {
        return wifiOther;
    }

    public void setWifiOther(boolean wifiOther) {
        this.wifiOther = wifiOther;
    }
}
